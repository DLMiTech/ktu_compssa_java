public class Variables {
    public static void main(String[] args) {
        //Declaring a variable
        int age;
        //Assignment of values
        age = 30;
        //Initialization of variables
        String name = "John";

        //Data type
        //Primitive data type
        //byte => -128 to 127
        byte num1 = 122;
        //short => -32,768 to 32,767
        short num2 = -32768;
        //int   => 2,147,483,784
        int num3 = 2_147_482_899;
        //long  => 9_000_000_000_000_000_000
        long num4 = 9_123_273_456_456_456_345L;
        //float => 5 to 6 dp
        float num5 = 3456.678F;
        //double    => 15 dp
        double num6 = 123.456;
        //char  => 'A', 'g', '3', '@'
        char ch = 'a';
        //boolean
        boolean isAStudent = true;


        //Non-Primitive data type
        //String
        String name1 = "Bob";
        //Array
        //Class
        //Object
        //Interface

        //name, myName, whatIsYourName,
        //Name, MyName, WhatIsYourName,
        //name, my_name, what_is_your_name


        //casting
        //Automatic
        double a = 10.0;
        int b = (int) a;
        System.out.println(b);



    }
}
