public class JavaOperations {
    public static void main(String[] args) {

        //Arithmetic (+, -, *, /, %, ++, --)
        int num1 = 27;
        int num2 = 5;

        System.out.println("10 + 3: "+(num1 + num2));
        System.out.println("10 - 3: "+(num1 - num2));
        System.out.println("10 * 3: "+(num1 * num2));
        System.out.println("10 / 3: "+((double) num1 / num2));
        System.out.println("10 %  3: "+(num1 * num2));
        System.out.printf("%d remainder %d\n", num1/num2, num1%num2);

        num1--;
        System.out.println(num1);

        //Assignment
        //Comparison
        //logical
    }
}
